import numpy as np

def main():
    arr = np.zeros((2, 3, 4, 5),  dtype=np.float32)
    count = 0
    for i in range(2):                  # Leftmost dimension has coarsest granularity.
        for j in range(3):
            for k in range(4):
                for l in range(5):      # Rightmost dimension has finest granularity.
                    arr[i][j][k][l] = count
                    count = count + 1
                    
    print(arr)
    
